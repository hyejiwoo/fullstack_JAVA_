package com.company.day007_operator;

public class Q006 {
	public static void main(String[] args) {
		System.out.println(10<3);
		// false
		System.out.println(10>=3);
		// true
		System.out.println(10<=3);
		// false
		System.out.println(10==3);
		// false
		System.out.println(10!=3);
		// true
	}
}
