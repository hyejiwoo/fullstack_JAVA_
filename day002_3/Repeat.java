package com.company.day002_3;

public class Repeat {
	public static void main(String[] args) {
		/* (String ab []) 실행 가능 */
		int a = 10;
		int b = 3;
		System.out.print(a + "+" + b + "=" + (a + b) + "\n");
		System.out.println(a + "+" + b + "=" + (a + b));
		System.out.printf("%d+%d=%d", a, b, a + b);
	}
}
